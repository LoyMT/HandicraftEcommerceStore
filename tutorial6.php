                    <tr>
                        <td><b>Steps:</b></td>
                        <td>
                            <ol>
                                <li>Insert a skewer into the spun cotton ball or egg to use as a handle.  </li>
                                <li>Mix a tiny amount of yellow paint into the water to tint it. </li>
                                <li>Brush the tinted water over the entire surface of the spun cotton. </li>
                                <li>Add a few drops of water to the accent color to water it down.</li>
                                <li>Lightly brush the accent color on the top and bottom of the lantern.</li>
                                <li>Then, blend the color toward the center of the lantern, using water to feather and soften the edge.</li>
                                <li>Set the skewer up in a jar or stick into a foam block to allow the shape to dry fully.</li>
                                <li>Paint floral or geometric designs on the lanterns using undiluted paint and a thin liner brush.
                                Patterns can be single sided, or continue around the lantern.</li>
                                <li>Let dry.</li>
                                <li>Cut a half-inch wide strip of black construction paper.</li>
                                <li>Fold it in half lengthwise, and then unfold. </li>
                                <li>Use sharp scissors to snip a wide fringe along the length, right up to the fold.</li>
                                <li>Wrap the un-fringed half of the paper strip around the cylinder, folding the fringe over the top.</li>
                                <li>Allow the end to overlap by about 1/4 inch, and snip off the rest.</li>
                                <li>Glue the end of the strip in place.</li> 
                                <li>Cut a circle the same size as your cylinder, and glue it on top of the cap.  Press it down, and then let it dry. </li>
                                <li>Use a thick pin to poke a hole in the center. </li>
                                <li>Use this method to make two caps for each lantern.</li>
                                <li>Snip two short lengths of 24 gauge or thinner floral wire.</li>
                                <li>Wrap each wire around a skewer to make a loop, and twist the ends together.</li>
                                <li>Make a tassel to hang from the lantern. </li>
                                <li>To make a foil tassel, cut a small rectangle of foil, about 1 x 1 1/4 inches, snip a fine fringe about 3/4 up to the top, and then roll up the uncut edge.</li>
                                <li>Cut an inch of red string, insert into the wire loop, fold it in half, and then glue into the tassel. </li>
                                <li>Feed the twisted ends of the wire through the paper caps. </li>
                                <li>Use the pin to make a hole in the bottom of the egg or ball. </li>
                                <li>Glue the caps in place on either end. </li>
                                <li>Add hanging loops to the tops of the ornaments.</li>
                            </ol>
