                        <td>
                            <ul>
                                <li>spun cotton ball 40mm, spun cotton egg 58mm</li>
                                <li>bamboo skewers, foam block or jar for holding the skewers</li>
                                <li>acrylic craft paint in light yellow, and other light base colors, plus colors for floral designs</li>
